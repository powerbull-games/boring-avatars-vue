<template>
    <svg
      :viewBox="'0 0 ' + SIZE + ' ' + SIZE"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      :width="size"
      :height="size"
    >
      <mask id="mask__sunset" maskUnits="userSpaceOnUse" :x="0" :y="0" :width="SIZE" :height="SIZE">
        <rect :width="SIZE" :height="SIZE" :rx="square ?  undefined : SIZE * 2" fill="white" />
      </mask>
      <g mask="url(#mask__sunset)">
        <path :fill="'url(#gradient_paint0_linear_' + _name + ')'" d="M0 0h80v40H0z" />
        <path :fill="'url(#gradient_paint1_linear_' + _name + ')'" d="M0 40h80v40H0z" />
      </g>
      <defs>
        <linearGradient
          :id="'gradient_paint0_linear_' + _name"
          :x1="SIZE / 2"
          :y1="0"
          :x2="SIZE / 2"
          :y2="SIZE / 2"
          gradientUnits="userSpaceOnUse"
        >
          <stop :stopColor="properties[0].color" />
          <stop :offset="1" :stopColor="properties[1].color" />
        </linearGradient>
        <linearGradient
          :id="'gradient_paint1_linear_' + _name"
          :x1="SIZE / 2"
          :y1="SIZE / 2"
          :x2="SIZE / 2"
          :y2="SIZE"
          gradientUnits="userSpaceOnUse"
        >
          <stop :stopColor="properties[2].color" />
          <stop :offset="1" :stopColor="properties[3].color" />
        </linearGradient>
      </defs>
    </svg>
</template>

<script>
import { getNumber, getRandomColor } from '../utilities';

export default {
    name: 'avatar-sunset',
    props: {
        name: {
            type: String,
            required: true
        },

        size: {
            type: Number,
            required: true
        },

        colors: {
            type: Array, 
            required: true
        },

        square: {
            type: Boolean,
            default: false
        }
    },

    data(){
        return {
            ELEMENTS: 4,
            SIZE: 80,
            _name: "",
            properties: {}
        }
    },

    beforeMount() {
      
        this.properties = this.generateColors(this.name, this.colors);
        this._name = this.name.replace(/\s/g, '');

    },

    methods: {

      generateColors(name, colors) {

        const numFromName = getNumber(name);
        const range = colors && colors.length;

        const elementsProperties = Array.from({ length: this.ELEMENTS }, (_, i) => ({
          color: getRandomColor(numFromName + i, colors, range),
        }));

        return elementsProperties;
      }

    }

}
</script>

